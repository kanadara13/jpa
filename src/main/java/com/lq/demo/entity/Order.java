package com.lq.demo.entity;

import javax.persistence.*;

@Entity
@Table(name = "user_order")
public class Order {

    @Id
    private Long id;

    @Column(name = "NAME")
    private String name;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
